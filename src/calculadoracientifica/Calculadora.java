    public void calculo() {
        switch (op) {
            case 1:
                result = num + Double.parseDouble(txtNum.getText());
                txtNum.setText(Double.toString(result));
                break;
            case 2:
                result = num - Double.parseDouble(txtNum.getText());
                txtNum.setText(Double.toString(result));
                break;
            case 3:
                result = num * Double.parseDouble(txtNum.getText());
                txtNum.setText(Double.toString(result));
                break;
            case 4:
                result = num / Double.parseDouble(txtNum.getText());
                txtNum.setText(Double.toString(result));
                break;
            default:
                JOptionPane.showMessageDialog(null, "Operacao Inv√°lida",JOptionPane.ICON_PROPERTY,JOptionPane.ERROR);
                break;
        }
    }

