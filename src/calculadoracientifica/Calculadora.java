    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        int tam = txtNum.getText().length();
        int num = txtNum.getText().length() - 1;
        String guarda;

        if (tam > 0) {
            StringBuilder apaga = new StringBuilder(txtNum.getText());
            apaga.deleteCharAt(num);
            guarda = apaga.toString();
            txtNum.setText(guarda);
        }


    }//GEN-LAST:event_btnBackActionPerformed

